# Jin (1978) walking-speed-in-smoke. These constants are real and independent
# of the FED model; the v1 read of them was not implicated in the exp error.
JIN_SLOPE_IRRITANT    = -0.44
JIN_SLOPE_NONIRRITANT = -0.22
JIN_INTERCEPT_A       = 8.42
JIN_INTERCEPT_B       = 6.13


def walking_speed(base_speed: float, Ks: float, irritant: bool = True) -> float:
    """Jin walking-speed reduction in smoke. Ks = extinction coeff (1/m)."""
    slope = JIN_SLOPE_IRRITANT if irritant else JIN_SLOPE_NONIRRITANT
    return max(base_speed + slope * Ks, 0.1)


def soot_to_extinction(soot_kg_m3, mass_extinction_coeff=8700.0):
    """FDB SOOT mass conc (kg/m^3) -> extinction coeff Ks (1/m)."""
    return mass_extinction_coeff * np.asarray(soot_kg_m3, dtype=float)


def eq_fatal(occupant_fed_totals, clip=True):
    """Scenario EQ_Fatal = expected-fatality sum over occupants. clip caps each
    occupant at 1.0. Zero-exposure scenarios give exactly 0.0."""
    fed = np.asarray(occupant_fed_totals, dtype=float)
    contrib = np.clip(fed, 0.0, 1.0) if clip else np.maximum(fed, 0.0)
    return float(contrib.sum())


def risk_index(frequency_per_yr, eq_fatal_value):
    """Per-scenario Risk Index = Frequency/yr * EQ_Fatal."""
    return frequency_per_yr * eq_fatal_value


# ============================================================================
# PART 3 — VALIDATION HARNESS (reproduces the fits used to confirm the model)
# ============================================================================
def validate_against_set(set_path, co_rmv_div_7_1=True):
    """Fit confirmed rates against a real .SET file; prints per-term R^2.
    Use this whenever you get a new .SET to re-check the bindings/flag."""
    rows = []
    for line in open(set_path, encoding="latin-1"):
        p = line.split()
        if len(p) >= 31:
            rows.append([float(x) for x in p[:31]])
    a = np.array(rows)
    if a.size == 0:
        print(f"{set_path}: no parseable rows"); return
    t = a[:, SET_COL["time_s"]] / 60.0
    m = a[:, SET_COL["FED_total"]] > 0
    if m.sum() == 0:
        print(f"{set_path}: no exposed occupants (control scenario)"); return

    def r2(rate, F):
        x = (rate * t)[m]; y = F[m]
        if np.dot(x, x) == 0: return float("nan"), float("nan")
        k = np.dot(x, y) / np.dot(x, x)
        return k, 1 - np.sum((y - k * x) ** 2) / np.sum((y - y.mean()) ** 2)

    rt = fed_rate(a[:, SET_COL["co"]], a[:, SET_COL["co2"]],
                  a[:, SET_COL["temp"]], co_rmv_div_7_1=co_rmv_div_7_1)
    k_co, r2_co     = r2(rt["FED_CO"],   a[:, SET_COL["FED_CO"]])
    k_ht, r2_ht     = r2(rt["FED_heat"], a[:, SET_COL["FED_heat"]])
    print(f"{set_path}: exposed={int(m.sum())}/{len(a)}")
    print(f"  CO   -> FED2: k={k_co:.4g}  R2={r2_co:+.3f}")
    print(f"  heat -> FED4: k={k_ht:.4g}  R2={r2_ht:+.3f}")


